package com.biggestworks.erasales;

import java.util.List;

public class ValueUser {
    String value;
    String message;


    Integer count;
    List<ResultUser> resultUser;


    public String getValue() {
        return value;
    }

    public String getMessage() {
        return message;
    }



    public Integer getCount() {
        return count;
    }

    public List<ResultUser> getResult() { return resultUser; }

}
